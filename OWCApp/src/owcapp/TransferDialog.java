    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(ValidateEntries())
        {
            String descriptionTo = "Transfer to " + toDepartment.getSelectedItem().toString() + " " + toAccount.getSelectedItem().toString() + " for " + description.getText();
            String descriptionFrom = "Transfer from " + fromDepartment.getSelectedItem().toString() + " " + fromAccount.getSelectedItem().toString() + " for " + description.getText();
            boolean isInternal = toAccount.getSelectedItem().toString().equals(fromAccount.getSelectedItem().toString());
            Transaction t = new Transaction(0, transDate.getText(), descriptionTo, Double.valueOf(amount.getText())*-1, comments.getText(),
                "Transfer", fromDepartment.getSelectedItem().toString(), receiptLink.getText(), "",
                0, false, isInternal);
            Broker b = Broker.getInstance();
            Status status = b.AddTransaction(t, fromAccount.getSelectedItem().toString()); // To transacion
            if(status.isSuccess)
            {
                // From transaction
                t.description = descriptionFrom;
                t.amount = Double.valueOf(amount.getText());
                t.department = toDepartment.getSelectedItem().toString();
                status = b.AddTransaction(t, toAccount.getSelectedItem().toString());
                if(status.isSuccess)
                {
                    JOptionPane.showMessageDialog(null, "Data Logged Successfully", "Data Logged Successfully", PLAIN_MESSAGE);
                    ClearData();
                }
                
                else
                {
                    JOptionPane.showMessageDialog(null, status.msg, "Database Query Error", ERROR_MESSAGE);
                    return;
                }   
                
            }

            else
            {
                JOptionPane.showMessageDialog(null, status.msg, "Database Query Error", ERROR_MESSAGE);
                return;
            }

        }       
        
    }//GEN-LAST:event_jButton2ActionPerformed

