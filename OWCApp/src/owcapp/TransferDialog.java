    private void ClearData()
    {
        description.setText("");
        transDate.setText("");
        amount.setText("");
        comments.setText("");
        receiptLink.setText("");
    }
    
    private boolean ValidateEntries()
    {
         String msg = new String();
            boolean isValid = ValidateDate(transDate.getText());
            if(!isValid)
            {
                msg += "Date format is invalid.\r\n";
            }
            
            if(Double.valueOf(amount.getText()) < 0.0)
            {
                msg += "Amount can't be less than 0.\r\n";
            }

            boolean isHundredthsPlace = IsAmountNearestHundreth(amount.getText());

            if(!isHundredthsPlace)
            {
                msg += "Amount isn't in money format.\r\n";
            }

            if(description.getText().isEmpty())
            {
                msg += "Reason for transfer can't be empty.\r\n";
            }
            
             if(!msg.isEmpty())
            {
                JOptionPane.showMessageDialog(null, msg, "Invalid Entry", ERROR_MESSAGE);
                isValid = false;
            }       
             
             return isValid;      

    }
